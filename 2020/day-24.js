function returnTile(route = "") {
  let x = 0;
  let y = 0;

  const moves = route.match(/(e|se|sw|w|nw|ne)/g);

  moves.forEach(move => {
    switch(move) {
      case "e":
        x += 2;
        break;
      case "se":
        x += 1;
        y -= 1;
        break;
      case "sw":
        y -=1;
        x -=1;
        break;
      case "w":
        x -=2;
        break;
      case "nw":
        x -= 1;
        y += 1;
        break;
      case "ne":
        x +=1;
        y +=1;
        break;
    }
  })

  return [x,y];
}

function figureOutPattern(raw = "") {
  const routes = raw.split("\n");
  const tiles = {};

  routes.forEach(route => {
    const [x,y] = returnTile(route);
    if (!tiles[x]) {
      tiles[x] = {};
    }
    if (tiles[x][y]) {
      tiles[x][y] = false;
    } else {
      tiles[x][y] = true;
    }
  });

  const count = Object.keys(tiles).map(key => tiles[key]).reduce((sum, row) => {
    return sum + Object.keys(row).map(key => row[key]).filter(x => x).length;
  }, 0)

  return count;
}

function getNeighbouringCoordinates([x,y] = [0,0]) {
  const changes = [
    [2,0],
    [1,-1],
    [1,1],
    [-2,0],
    [-1,-1],
    [-1,1]
  ];

  return changes.map(([changeX, changeY]) => {
    return [x + changeX, y + changeY];
  })
}

function runRound(state = {}, log) {
  const tilesToCheck = [];
  const tilesThatHaveBeenChecked = [];
  const output = {};
  let i = 0;

  Object.keys(state).forEach(x => {
    Object.keys(state[x]).forEach(y => {
      if (state[x][y]) {
        tilesToCheck.push([parseInt(x, 10), parseInt(y, 10)]);
      }
    });
  });

  while(tilesToCheck.length > 0 && i < 100000) {
    i ++;
    const toCheck = tilesToCheck.pop();
    if (tilesThatHaveBeenChecked.indexOf(toCheck.join(",")) === -1) {
      const [x,y] = toCheck;
      if (!output[x]) {
        output[x] = {};
      }
      tilesThatHaveBeenChecked.push(toCheck.join(","));
      const neighbouringCoordinates = getNeighbouringCoordinates([x,y]);
      const neighbouringBlackCount = neighbouringCoordinates.reduce((sum, [corX, corY]) => {
        if (state[corX] && state[corX][corY]) {
          return sum + 1;
        } else {
          return sum;
        }
      },0);
      const isBlack = state[x] ? state[x][y] : false;

      if (isBlack) {
        if (neighbouringBlackCount === 0 || neighbouringBlackCount > 2) {
          output[x][y] = false;
        } else {
          output[x][y] = true;
        }
        neighbouringCoordinates.forEach(coordinate => tilesToCheck.push(coordinate));
      } else if (neighbouringBlackCount === 2) {
        output[x][y] = true;
      }
    } 
  }

  return output;
}

