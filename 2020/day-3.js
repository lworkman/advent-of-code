function checkSpotForTree(position = 0, row = "") {
  const actualPosition = position % row.length;

  return row[actualPosition] === "#";
}

function goDownSlope(slope = [], multiplier = 1, iterator = 1) {
  let cursor = 0;
  let treeCount = 0;

  while (cursor * iterator < slope.length) {
    if (checkSpotForTree(cursor * multiplier, slope[cursor * iterator])) {
      treeCount++;
    }
    cursor++;
  }

  return treeCount;
}

function goDownMultipleRoutes(slope = []) {
  const routes = [
    [1, 1],
    [3, 1],
    [5, 1],
    [7, 1],
    [1, 2],
  ];

  return routes
    .map((route) => goDownSlope(slope, route[0], route[1]))
    .reduce((product, num) => {
      return product * num;
    }, 1);
}

