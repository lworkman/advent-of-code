function calculateRound(raw = "") {
  const codes = raw.split("\n");
  let twoCharCount = 0;
  let threeCharCount = 0;

  codes.forEach((code) => {
    const [twoChar, threeChar] = countAmount(code);

    twoCharCount += twoChar;
    threeCharCount += threeChar;
  });

  console.log(twoCharCount, threeCharCount);

  return twoCharCount * threeCharCount;
}

function countAmount(code = "") {
  let hasTwoChars = false;
  let hasThreeChars = false;
  const chars = {};

  code.split("").forEach((char) => {
    if (!chars[char]) {
      chars[char] = 0;
    }
    chars[char]++;
  });

  Object.keys(chars).forEach((key) => {
    if (chars[key] === 2) {
      hasTwoChars = true;
    } else if (chars[key] === 3) {
      hasThreeChars = true;
    }
  });

  return [hasTwoChars ? 1 : 0, hasThreeChars ? 1 : 0];
}

function findSimilar(raw = "") {
  const codes = raw.split("\n");
  let codeToCheck = codes.shift();
  let match;

  while (!match && codeToCheck) {
    const splitParent = codeToCheck.split("");

    for (let i = 0; i < codes.length; i++) {
      const splitToCheck = codes[i].split("");
      let count = 0;
      for (let x = 0; x < splitParent.length; x++) {
        if (splitParent[x] !== splitToCheck[x]) {
          count++;
        }

        if (count > 1) {
          break;
        }
      }
      if (count === 1) {
        match = [codeToCheck, codes[i]];
        break;
      }
    }

    codeToCheck = codes.shift();
  }

  if (match) {
    let output = [];

    match[0].split("").forEach((char, index) => {
      if (match[1][index] === char) {
        output.push(char);
      }
    });

    return output.join("");
  }
}

